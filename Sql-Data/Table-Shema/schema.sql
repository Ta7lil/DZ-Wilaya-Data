CREATE TABLE WILAYA_FR(
    name_fr VARCHAR(50) PRIMARY KEY
);
CREATE TABLE WILAYA_CODE_TEL(
    code_tel VARCHAR(50) PRIMARY KEY
);
CREATE TABLE WILAYA_CODE_POST(
    code_post VARCHAR(50) PRIMARY KEY
);
CREATE TABLE WILAYA_CODE(
    code VARCHAR(50) PRIMARY KEY
);
CREATE TABLE WILAYA_AR(
    name_ar VARCHAR(50) PRIMARY KEY
);
CREATE TABLE WILAYA(
    code VARCHAR(50) PRIMARY KEY,
    code_tel VARCHAR(50) NOT NULL,
    code_post VARCHAR(50) NOT NULL,
    name_ar VARCHAR(50) NOT NULL,
    name_fr VARCHAR(50) NOT NULL,
    name_en VARCHAR(50) NOT NULL
);
CREATE TABLE BALADIYA_FR(
    name_fr VARCHAR(50) PRIMARY KEY
);
CREATE TABLE BALADIYA_CODE(
		    code VARCHAR(50) PRIMARY KEY
		);
CREATE TABLE BALADIYA_AR(
    name_ar VARCHAR(50) PRIMARY KEY
);
CREATE TABLE BALADIYA(
    code VARCHAR(50) PRIMARY KEY,
    name_ar VARCHAR(50) NOT NULL,
    name_fr VARCHAR(50) NOT NULL,
    name_en VARCHAR(50) NOT NULL,
    wilaya_code VARCHAR(50) NOT NULL,
    FOREIGN KEY(wilaya_code) REFERENCES WILAYA(code)
);
